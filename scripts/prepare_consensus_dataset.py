#!/usr/bin/env python3
"""Prepare a flat consensus working dataset from a sample manifest.

Creates:
- <output>/images           (already created by sampler; script validates)
- <output>/transcriptions   (empty placeholder JSON files per stem)

The placeholder transcriptions allow reuse of existing flat-directory tooling.
"""

from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Prepare consensus dataset layout")
    parser.add_argument(
        "--manifest-csv",
        type=Path,
        default=Path("outputs/consensus_dataset_1000/sample_manifest.csv"),
        help="Manifest CSV generated by sample_unseen_images.py",
    )
    parser.add_argument(
        "--dataset-root",
        type=Path,
        default=Path("outputs/consensus_dataset_1000"),
        help="Dataset root containing images/ and transcriptions/",
    )
    parser.add_argument(
        "--overwrite-placeholders",
        action="store_true",
        help="Rewrite existing placeholder transcriptions",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    dataset_root = args.dataset_root.resolve()
    images_dir = dataset_root / "images"
    transcriptions_dir = dataset_root / "transcriptions"
    manifest = args.manifest_csv.resolve()

    if not manifest.exists():
        raise SystemExit(f"Manifest not found: {manifest}")
    if not images_dir.exists():
        raise SystemExit(f"Images directory not found: {images_dir}")

    transcriptions_dir.mkdir(parents=True, exist_ok=True)

    created = 0
    skipped = 0

    with manifest.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            stem = row.get("stem", "").strip()
            if not stem:
                continue
            out_file = transcriptions_dir / f"{stem}.json"
            if out_file.exists() and not args.overwrite_placeholders:
                skipped += 1
                continue

            # Placeholder structure compatible with existing grid parser.
            payload = {f"Day {i}": [None] * 12 for i in range(1, 32)}
            payload["Totals"] = [None] * 12
            out_file.write_text(json.dumps(payload, indent=2), encoding="utf-8")
            created += 1

    print(f"Dataset root: {dataset_root}")
    print(f"Images dir: {images_dir}")
    print(f"Transcriptions dir: {transcriptions_dir}")
    print(f"Placeholders created: {created}")
    print(f"Placeholders skipped: {skipped}")


if __name__ == "__main__":
    main()
